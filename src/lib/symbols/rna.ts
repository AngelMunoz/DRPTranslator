/**
 * Enumerable that represents the RNA bases
 * @see https://en.wikipedia.org/wiki/RNA#Stucture
 */
export enum RNA {
    /**
     * @name Adenine
     */
    A = "A",
    /**
     * @name Uracil
     */
    U = "U",
    /**
     * @name Guanine
     */
    G = "G",
    /**
     * @name Cytosine
     */
    C = "C",
}
