/**
 * Enumerable that represents the DNA bases
 * @see https://en.wikipedia.org/wiki/DNA#Properties
 */
export enum DNA {
    /**
     * Adenine
     */
    A,
    /**
     * Thymine
     */
    T,
    /**
     * Guanine
     */
    G,
    /**
     * Cytosine
     */
    C
}
